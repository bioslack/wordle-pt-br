class Line {
  constructor() {
    this.cells = [];
    this.word = "";
    this.element = document.createElement("div");
    this.element.classList.add("line");

    this.appendCell();
    this.appendCell();
    this.appendCell();
    this.appendCell();
    this.appendCell();
  }

  appendCell() {
    const cell = new Cell();
    this.element.appendChild(cell.element.getRootNode());
    this.cells.push(cell);
  }

  removeLetter() {
    if (this.word.length > 0) {
      this.cells[this.word.length - 1].clear();
      this.word = this.word.substring(0, this.word.length - 1);
    }
  }

  setLetter(letter) {
    if (this.word.length < 5) {
      this.word = this.word + letter;
      this.cells[this.word.length - 1].setLetter(letter);
    }
  }

  reveal(answer) {
    const cells = this.cells;
    const word = words
      .find((w) => this.word === removeSpecialCharacters(w).toLowerCase())
      .toLowerCase();

    const cleanAnswer = removeSpecialCharacters(answer).toLowerCase();
    const toBeFound = cleanAnswer.split("");
    const exactChars = [...toBeFound].filter((chr, index) => {
      if (this.word[index] === chr)
        toBeFound.indexOf(chr) !== -1 &&
          toBeFound.splice(toBeFound.indexOf(chr), 1);
      return this.word[index] === chr;
    });

    const correctNotExactChars = word.split("").filter((chr, index) => {
      const condition =
        chr !== cleanAnswer[index] && toBeFound.includes(this.word[index]);
      if (condition)
        toBeFound.indexOf(chr) !== -1 &&
          toBeFound.splice(toBeFound.indexOf(chr), 1);
      return condition;
    });

    cells.forEach(function (element, index) {
      const letter = element.letter;

      if (letter === cleanAnswer[index]) {
        element.reveal("correct-position", word[index]);
        exactChars.indexOf(letter) !== -1 &&
          exactChars.splice(exactChars.indexOf(letter), 1);
      } else if (correctNotExactChars.includes(letter)) {
        element.reveal("correct-letter", word[index]);
        correctNotExactChars.indexOf(letter) !== -1 &&
          correctNotExactChars.splice(correctNotExactChars.indexOf(letter), 1);
      } else {
        element.reveal("wrong-letter", word[index]);
      }
    });

    return this.word === cleanAnswer;
  }
}

